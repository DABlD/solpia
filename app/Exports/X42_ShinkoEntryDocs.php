    public function __construct($data, $type, $rank = false){
        foreach(['document_id', 'document_flag', 'document_lc', 'document_med', 'document_med_cert' ] as $docuType){
            foreach($data->$docuType as $key => $doc){
                $name = $doc->type;
                if(!isset($data->$docuType->$name)){
                    $data->$docuType->$name = $doc;
                }
                else{
                    $size = 0;
                    if(is_array($data->$docuType->$name)){
                        $size = sizeof($data->$docuType->$name);
                    }
                    $name .= $size;
                    $data->$docuType->$name = $doc;
                }
                $data->$docuType->forget($key);
            }
        }

        $this->data     = $data;
        $this->type     = $type;
        $this->rank     = $rank;
    }

    public function view(): View
    {
        return view('exports.forms.' . lcfirst($this->type), [
            'data' => $this->data,
        ]);
